CREATE DATABASE BDESTACIONAMENTO;

USE BDESTACIONAMENTO;

CREATE TABLE TBCATEGORIA (
IDCAT INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
VAGACAT VARCHAR(12) NOT NULL,
PREFERENCIALCAT CHAR(1) NOT NULL,
PRECOCAT DECIMAL(5,2) NOT NULL
);

CREATE TABLE TBVAGA (
IDVAGA INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
CODCAT INT NOT NULL,
NOMEVAGA VARCHAR(10) NOT NULL,
STATUSVAGA VARCHAR(7) NOT NULL,
FOREIGN KEY (CODCAT) REFERENCES TBCATEGORIA (IDCAT)
)ENGINE=INNODB;

CREATE TABLE TBCLIENTE (
IDCLIENTE INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
NOMECLIENTE VARCHAR(30) NOT NULL,
CREDENCLIENTE CHAR(1) NOT NULL,
TELEFONECLIENTE VARCHAR(14) NOT NULL,
CPFCLIENTE VARCHAR(14) NOT NULL UNIQUE
);

CREATE TABLE TBVEICULO (
IDVEICULO INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
CODCLIENTE INT NOT NULL,
PLACAVEICULO VARCHAR (7) NOT NULL,
MODELOVEICULO VARCHAR(15) NOT NULL,
TIPOVEICULO VARCHAR(10) NOT NULL,
FOREIGN KEY (CODCLIENTE) REFERENCES TBCLIENTE (IDCLIENTE)
)ENGINE=INNODB;

CREATE TABLE TBFUNCIONARIO (
IDFUNCI INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
DATACONT DATE NOT NULL,
SENHAFUNCI VARCHAR(30) NOT NULL,
TELEFONEFUNCI VARCHAR(14) NOT NULL,
EMAILFUNCI VARCHAR(50) NOT NULL,
CPFFUNCI VARCHAR(14) NOT NULL UNIQUE,
NOMEFUNCI VARCHAR(30) NOT NULL
);

CREATE TABLE TBSERVICO (
IDSERVICO INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
CODVAGA INT NOT NULL,
CODVEICULO INT NOT NULL,
CODFUNCI INT NOT NULL,
DATAFINAL DATE NOT NULL,
DATAINICIAL DATE NOT NULL,
HORAENTRADA TIME NOT NULL,
HORASAIDA TIME NOT NULL,
FOREIGN KEY (CODVAGA) REFERENCES TBVAGA (IDVAGA),
FOREIGN KEY (CODVEICULO) REFERENCES TBVEICULO (IDVEICULO),
FOREIGN KEY (CODFUNCI) REFERENCES TBFUNCIONARIO (IDFUNCI)
)ENGINE=INNODB;

